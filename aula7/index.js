/* 
- Aritméticos {+ - / *} 
- (+) Adição/Concatenação
- (-) Subtração
- (/) Divisão
- (*) Multiplicação
- (**) Potenciação
- (%) Resto da divisão
*/

const num1 = 10;
const num2 = 5;
console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 / num2);
console.log(num1 * num2);
console.log(num1 ** num2);
console.log(num1 % num2);

/* 
- (++) Adiciona +1 ao valor dela
- (--) Diminui -1 ao valor dela
- (+=) Conta +1 de forma automática
- (*=) Multiplica
- (**=) Exponêncial
*/
let contador = 1;
contador ++;
console.log(contador);

let contadorMenos = 1;
contadorMenos --;
console.log(contadorMenos);

const passo = 2;
let contadorPasso = 0
contadorPasso += passo;
console.log(contadorPasso)

